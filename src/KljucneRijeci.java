public class KljucneRijeci {
    public static void main(String[] args){
        System.out.println("Bok svima");
    }
}

// public - omogucava da Java pokrene ovu metodu bez obzira gde se koristi
// static - kaze da metoda pripada klasi, a ne instanci klase. Zato se može pokrenuti direktno
// void - oznacava da metoda ne vraća nikakvu vrednost, to znaci da metoda ne daje nikakav rezultat
// main -  sluzi kao ulazna tocka za pokretanje programa
// Ova kombinacija (public static void main) je potrebna za pokretanje bilo kog Java programa.